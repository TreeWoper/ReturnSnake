public class Snake {
    private int row;
    private int column;
    
    public Snake(int r, int c){
        row = r;
        column = c;
    }

    public int getColumn() {
        return column;
    }

    public int getRow() {
        return row;
    }

    public void setColumn(int column) {
        this.column = column;
    }

    public void setRow(int row) {
        this.row = row;
    }
}
